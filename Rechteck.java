public class Rechteck {
    private int laenge;
    private int breite;
    
    public Rechteck(int laenge, int breite) {
        this.laenge = laenge;
        this.breite = breite;
    }
    
    public void setLaenge(int laenge) {
        this.laenge = laenge;
    }
    
    public int getLaenge() {
        return this.laenge;
    }
}